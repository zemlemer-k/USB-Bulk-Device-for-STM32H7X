/**
 ***********************************************************************************************************************
 * \brief   initLeds
 * \details Debug leds initialization; Led pin - PA1
 * \param   nothing
 * \retval  Error code:     0 -- Ok
 ***********************************************************************************************************************
**/
int initLeds(void)
{
    // Clocks initialization
    __HAL_RCC_GPIOA_CLK_ENABLE();
    GPIO_InitStruct.Pin = GPIO_PIN_1;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
    HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

    // Closing analog switch on PA1 and PA1_C
    HAL_SYSCFG_AnalogSwitchConfig(SYSCFG_SWITCH_PA1, SYSCFG_SWITCH_PA1_CLOSE);

    return 0;
}

/**
 ***********************************************************************************************************************
 * \brief   setLedState
 * \details Setting new led state
 * \param   tDbgLedState ledState: setting new led stste
 * \retval  nothing
 ***********************************************************************************************************************
**/
void setLedState(tDbgLedState ledState)
{
    if(dbgLedOn == ledState) {
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_RESET);
    }
    else {
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_SET);
    }
}


/**
 ***********************************************************************************************************************
 * \brief   toggleLedState
 * \details Switching debug led to the next state
 * \param   nothing
 * \retval  nothing
 ***********************************************************************************************************************
**/
void toggleLedState(void)
{
    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_1);
}

